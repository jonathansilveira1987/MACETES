print(f'\n{x.strftime("%d")}') # Dia
print(f'\n{x.strftime("%B")}') # Mês
print(f'\n{x.strftime("%m")}') # Número do mês
print(f'\n{x.strftime("%Y")}') # Ano
print(f'\n{x.strftime("%H")}') # Hora
